print("========= Python Escape Sequences =========\n")

print("1. Newline (\\n):")
print("Hello\nWorld")

print("\n2. Tab (\\t):")
print("Name:\tPython")

print("\n3. Backslash (\\\\):")
print("This is a backslash: \\")

print("\n4. Single Quote (\\'):")
print('It\'s Python!')

print("\n5. Double Quote (\\\"):")
print("He said, \"Python is awesome!\"")

print("\n6. Carriage Return (\\r):")
print("Python\r12345")  # Overwrites the beginning with 12345

print("\n7. Backspace (\\b):")
print("Hello\bWorld")  # Removes the 'o'

print("\n8. Form Feed (\\f):")
print("Hello\fWorld")  # New page/feed effect (mostly visible in printers)

print("\n9. Vertical Tab (\\v):")
print("Hello\vWorld")  # Vertical tab space between lines

print("\n10. Null Character (\\0):")
print("Python\0Rocks")  # \0 is a null char, behaves like empty character

print("\n11. Unicode character (\\uXXXX):")
print("Unicode heart: \u2764")  # ‚ù§

print("\n12. Hexadecimal (\\xhh):")
print("Character for \\x41: \x41")  # A

print("\n============================================")
